# Renderer registry
RENDERERS: dict[str, type[RuntimeRenderer]] = {
    "hermes": SkillMarkdownRenderer,
    "openclaw": OpenClawRenderer,
    "cursor": CursorRenderer,
    "windsurf": WindsurfRenderer,
    "aider": AiderRenderer,
}


def get_renderer(target: str) -> RuntimeRenderer | None:
    """Get the appropriate renderer for a target Agent."""
    cls = RENDERERS.get(target)
    return cls() if cls else None
