from __future__ import annotations

import json
import shutil
from pathlib import Path

from agentmesh import __version__
from agentmesh.services.registry_service import list_registry_skills
from agentmesh.utils.frontmatter import read_skill_document


def _skill_entry(skill_dir: Path, out_root: Path) -> dict:
    entrypoint = skill_dir / "SKILL.md"
    doc = read_skill_document(entrypoint)
    name = str(doc.metadata.get("name") or skill_dir.name)
    description = str(doc.metadata.get("description") or "")
    rel = (out_root / "skills" / skill_dir.name / "SKILL.md").relative_to(out_root).as_posix()
    return {"name": name, "path": rel, "description": description}


def export_claude_code_package(agentmesh_home: Path, out: Path) -> dict:
    skills = list_registry_skills(agentmesh_home)
    if out.exists():
        shutil.rmtree(out)
    (out / "skills").mkdir(parents=True, exist_ok=True)

    entries = []
    for skill in skills:
        target = out / "skills" / skill.name
        shutil.copytree(skill, target)
        entries.append(_skill_entry(skill, out))

    plugin = {
        "schema": "agentmesh.claude-code-plugin/v1",
        "name": "agentmesh-skills",
        "version": __version__,
        "description": "AgentMesh exported skills package for Claude Code.",
        "skills": [{"name": item["name"], "path": item["path"]} for item in entries],
    }
    (out / "plugin.json").write_text(
        json.dumps(plugin, ensure_ascii=False, indent=2) + "\n", encoding="utf-8"
    )
    (out / "README.md").write_text(_readme(entries), encoding="utf-8")
    return {
        "target": "claude-code",
        "out": str(out),
        "skills": [item["name"] for item in entries],
        "plugin": str(out / "plugin.json"),
    }


def _readme(entries: list[dict]) -> str:
    lines = [
        "# AgentMesh Claude Code Skills Package",
        "",
        "This package was generated by AgentMesh.",
        "",
        "## Skills",
        "",
    ]
    for item in entries:
        desc = f" — {item['description']}" if item.get("description") else ""
        lines.append(f"- `{item['name']}`{desc}")
    lines.append("")
    return "\n".join(lines)
